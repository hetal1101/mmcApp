package com.makemusiccount.android.model;

public class TutorialEquationList {
    String sr, label, value, key_value, octave, hint;
    String eqn_type;
    String eqn_image;

    public String getEqn_type() {
        return eqn_type;
    }

    public void setEqn_type(String eqn_type) {
        this.eqn_type = eqn_type;
    }

    public String getEqn_image() {
        return eqn_image;
    }

    public void setEqn_image(String eqn_image) {
        this.eqn_image = eqn_image;
    }

    public String getHint() {
        return hint;
    }

    public void setHint(String hint) {
        this.hint = hint;
    }

    public String getSr() {
        return sr;
    }

    public void setSr(String sr) {
        this.sr = sr;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getKey_value() {
        return key_value;
    }

    public void setKey_value(String key_value) {
        this.key_value = key_value;
    }

    public String getOctave() {
        return octave;
    }

    public void setOctave(String octave) {
        this.octave = octave;
    }
}
